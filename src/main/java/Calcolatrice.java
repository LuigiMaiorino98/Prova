public class Calcolatrice {

    private int x;
    private int y;


    public Calcolatrice(int x , int y){
      this.x=x;
      this.y=y;
    }

    public int somma (int x,int y){
        return x+y;
    }

    public int sottrazione (int x,int y){
        return x-y;
    }

    public int divisione (int x,int y){
        return x/y;
    }

    public int moltiplicazione (int x,int y){
        return x*y;
    }




}
